package com.example.app.ui.util

/**
 * Bike navigation type
 *
 * @constructor Create empty Bike navigation type
 */
enum class BikeNavigationType {
    /**
     * Bottom Navigation
     *
     * @constructor Create empty Bottom Navigation
     */
    BOTTOM_NAVIGATION,

    /**
     * Navigation Rail
     *
     * @constructor Create empty Navigation Rail
     */
    NAVIGATION_RAIL,

    /**
     * Permanent Navigation Drawer
     *
     * @constructor Create empty Permanent Navigation Drawer
     */
    PERMANENT_NAVIGATION_DRAWER
}